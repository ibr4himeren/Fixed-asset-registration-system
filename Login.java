    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {                                         
        try {
            String email = txtemail.getText();
            String password = txtpassword.getText();

            if (email.isEmpty() || password.isEmpty()) {
                throw new IllegalArgumentException("Email and password cannot be empty");
            }

            Employee rEmployee = DBHelper.SignIn(email, password);

            if (rEmployee != null) {
                if (rEmployee.Employeetype.equals("Manager")) {
                    ManagerHome frmmanager = new ManagerHome();
                    frmmanager.setVisible(true);
                    this.dispose();
                } else if (rEmployee.Employeetype.equals("Staff")) {
                    StaffHome frmstaff = new StaffHome();
                    frmstaff.setVisible(true);
                    this.dispose();
                }
            } else {
                txtemail.setText("");
                txtpassword.setText("");
                JOptionPane.showMessageDialog(this, "Invalid email or password", "Login Failed", JOptionPane.WARNING_MESSAGE);
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Please enter both email and password", "Invalid input", JOptionPane.WARNING_MESSAGE);
        }
        // TODO add your handling code here:
    }                                        

